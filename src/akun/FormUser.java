    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try { 
        if (txtUsername.getText().trim().equals("") || txtPW.getText().trim().equals("")|| cbBagian.getSelectedItem().equals("")||cbBagian.getSelectedItem().equals("--Pilih--")){
            msgEmpty();
        }else{
            generateID();   
            String SQL = "INSERT INTO login VALUES ('" + txtId.getText() + "','" + txtUsername.getText() +"','" + txtPW.getText() +"','" + cbBagian.getSelectedItem() + "')";
            st.executeUpdate(SQL);
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            setEmpty();
            setData(MySQL);
        }
        } catch (Exception e){
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        try {
        if ((txtId.getText().equals("")) || txtUsername.getText().equals("") || txtPW.getText().equals("")|| cbBagian.getSelectedItem().equals("")){
            msgEmpty();
        }else{
            String SQL = "UPDATE login SET username = '" + txtUsername.getText() + "', password = '" + txtPW.getText() +  "', bagian = '" + cbBagian.getSelectedItem() + "' WHERE id_user = '" + txtId.getText() + "'";
            st.executeUpdate(SQL);
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            setEmpty();
            setData(MySQL);
            }
        } catch (Exception e){
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        try{
        if ((txtId.getText().equals(""))){
            JOptionPane.showMessageDialog(null, "Id Kosong Pilih Data Di Tabel");
        }else{ 
            if (JOptionPane.showConfirmDialog(null, "Yakin Ingin Menghapus Data Dengan ID "+txtId.getText()+"", "Confrim", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)== JOptionPane.YES_OPTION) {
                try{
                    st = con.createStatement();
                    String sql = "DELETE FROM login WHERE id_user = '" + txtId.getText() + "'";
                    st.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                    setEmpty();
                    setData(MySQL);
                }catch (Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
        }
        }
        } catch (Exception e){
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_btnHapusActionPerformed

