    private void setEmpty(){
        txtId.setText("");
        txtUsername.setText("");
        txtPW.setText("");
        search.clearSelection();
        cbBagian.setSelectedItem("--Pilih--");
    }
    
    private void msgEmpty(){
        if (txtUsername.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Username Kosong");
            txtUsername.requestFocus();
        }else if (txtPW.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null, "Password Kosong");
            txtPW.requestFocus();
        }else if(cbBagian.getSelectedItem().equals("--Pilih--")){
            JOptionPane.showMessageDialog(null, "Pilih Bagian");
            cbBagian.requestFocus();
        }else if(cbBagian.getSelectedItem().equals("")){
            JOptionPane.showMessageDialog(null, "Pilih Bagian");
            cbBagian.requestFocus();
        }
    }
    
     private void setData(String SQL){
        try {
            st = con.createStatement();
            rs = st.executeQuery(SQL);
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("ID");
            model.addColumn("USERNAME");
            model.addColumn("PASSWORD");
            model.addColumn("BAGIAN");
            
            model.getDataVector().removeAllElements();
            model.fireTableDataChanged();
            model.setRowCount(0);
            
            while(rs.next()){
                Object[] data = {
                  rs.getString("id_user"),
                  rs.getString("username"),
                  rs.getString("password"),
                  rs.getString("bagian")
                };
                model.addRow(data);
                tabelUser.setModel(model);
            }
        } catch (SQLException e){
            
        }
    }
     
    private void search() throws SQLException{
        String msg;
        
        if (rbId.isSelected()){
            msg = JOptionPane.showInputDialog("Masukan ID User");
            if (msg.trim().equalsIgnoreCase("")){
                setEmpty();
                setData(MySQL);
            }else{
                txtId.setText(msg);
                String SQL = "SELECT * from login WHERE id_user LIKE '%"+txtId.getText()+"%'";
                setData(SQL);   
            }
        }else if (rbUsername.isSelected()){
            msg = JOptionPane.showInputDialog("Masukan Username");
            if (msg.trim().equalsIgnoreCase("")){
                setEmpty();
                setData(MySQL);
            }else{
                txtUsername.setText(msg);
                String SQL = "SELECT * from login WHERE username LIKE '%"+txtUsername.getText()+"%'";
                setData(SQL);
            }
        }else if (rbBagian.isSelected()){
            msg = JOptionPane.showInputDialog("Masukan Bagian");
            if (msg.trim().equalsIgnoreCase("")){
                setEmpty();
                setData(MySQL);
            }else{
                String SQL = "SELECT * from login WHERE bagian LIKE '%"+msg+"%'";
                setData(SQL);
            }
        }else {
            JOptionPane.showMessageDialog(null, "Silahkan Checklist Pencarian");
        }
        
    }
    
    
