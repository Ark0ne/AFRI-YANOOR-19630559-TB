            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/db_kepegawaian","root","");
            String SQL = "select * from login where username='"+txtUsername.getText()+"' and password='"+String.valueOf(txtPW.getText())+"' and bagian='"+cbBagian.getSelectedItem()+"'";
