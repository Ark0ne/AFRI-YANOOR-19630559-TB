    private void seleksiLogin(){
        if (cbBagian.getSelectedItem().equals("Tata Usaha")){
            new FormMenu().pegawai.enable(true);
            new FormMenu().admin.enable(false);
            new FormMenu().laporan.enable(true);
            new FormMenu().logIn.enable(false);
            new FormMenu().logOut.enable(true);
        }else if (cbBagian.getSelectedItem().equals("Admin")){
            new FormMenu().admin.enable(true);
            new FormMenu().pegawai.enable(false);
            new FormMenu().laporan.enable(true);
            new FormMenu().logIn.enable(false);
            new FormMenu().logOut.enable(true);
        }    
    }

    private void setCancel(){
        txtUsername.setText("");
        txtPW.setText("");
        cbBagian.setSelectedIndex(0);
    }

    private void showPW(){
        if (chkPW.isSelected()){
            txtPW.setEchoChar((char)0);    
        }else{
            txtPW.setEchoChar('*');
        }
    }
    
